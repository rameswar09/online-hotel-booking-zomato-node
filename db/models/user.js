const mongoose = require("mongoose");

const user = mongoose.model("USER", {
  code: String,
  name: {
    type: String,
    required: true,
    trim: true,
  },
  email: {
    type: String,
    required: true,
    trim: true,
    lowercase: true,
  },
  password: {
    type: String,
    required: true,
  },
  tokens: [
    {
      token: {
        type: String,
        required: true,
      },
    },
  ],
  time: { type: Date, default: Date.now },
});

module.exports = user;
